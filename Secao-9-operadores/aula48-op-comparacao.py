# ==  # Equal
# !=  # Not equal
# >   # Greater than
# <   # Less than
# >=  # Greater than or equal to
# <=  # Less than or equal to

opEqual = "banana" == "Banana"
print(opEqual) # False

opNotEqual = "banana" != "banana"
print(opNotEqual) # True

################

opGreater = 10 > 5
print(opGreater) # True